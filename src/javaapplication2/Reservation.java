    private void loadReservations() {
        // Define column headers if not set in the GUI Builder
        String[] columnHeaders = {"ID", "Client", "Vehicle", "Start Date", "End Date", "Total Amount"};

        // Get the table model
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        tableModel.setRowCount(0); // Clear existing rows
        tableModel.setColumnIdentifiers(columnHeaders); // Ensure headers are set

        String query = """
            SELECT r.id_reservation, c.nom, c.prenom, v.marque, v.modele, r.date_debut, r.date_fin, r.montant_total
            FROM reservation r
            JOIN client c ON r.id_client = c.id_client
            JOIN vehicule v ON r.id_vehicule = v.id_vehicule
        """;

        try (Connection conn = DatabaseConnection.connect();
             Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery(query)) {

            // Add rows to the table model
            while (rs.next()) {
                int idReservation = rs.getInt("id_reservation");
                String clientName = rs.getString("nom") + " " + rs.getString("prenom");
                String vehicleDetails = rs.getString("marque") + " " + rs.getString("modele");
                String startDate = rs.getDate("date_debut").toString();
                String endDate = rs.getDate("date_fin").toString();
                double totalAmount = rs.getDouble("montant_total");

                Object[] rowData = {idReservation, clientName, vehicleDetails, startDate, endDate, totalAmount};
                tableModel.addRow(rowData);
            }

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading reservations: " + e.getMessage());
        }
    }
    

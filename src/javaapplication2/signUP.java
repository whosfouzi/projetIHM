    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String nom = jTextField1.getText();
        String prenom = jTextField2.getText();
        String numPermis = jTextField4.getText();
        String email = jTextField3.getText();
        String motDePasse = new String(jPasswordField1.getPassword());
        String hashedPassword = BCrypt.hashpw(motDePasse, BCrypt.gensalt()); // Hachage du mot de passe

        String role = null;
        
        if (jRadioButton4.isSelected()) {
            role = "Admin";
        } else if (jRadioButton5.isSelected()) {
            role = "Manager";
        } else if (jRadioButton6.isSelected()) {
            role = "Client";
        }

            // VÃ©rifier que tous les champs sont remplis
        if (nom.isEmpty() || prenom.isEmpty() || numPermis.isEmpty() || email.isEmpty() || motDePasse.isEmpty() || role.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Veuillez remplir tous les champs !", "Erreur", JOptionPane.ERROR_MESSAGE);
            return;
        }
    
        // Connexion Ã  la base de donnÃ©es
        String url = "jdbc:mysql://localhost:3306/projetihm";
        String user = "root"; // Ton nom d'utilisateur
        String password = ""; // Ton mot de passe
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            try (Connection conn = DriverManager.getConnection(url, user, password)) {
                // PrÃ©parer la requÃªte SQL
                String sql = "INSERT INTO utilisateur (nom, prenom, num_permis, email, mot_de_passe, role) VALUES (?, ?, ?, ?, ?, ?)";
                PreparedStatement statement = conn.prepareStatement(sql);
                statement.setString(1, nom);
                statement.setString(2, prenom);
                statement.setString(3, numPermis);
                statement.setString(4, email);
                statement.setString(5, hashedPassword); // Tu peux aussi hacher le mot de passe avec une mÃ©thode comme BCrypt
                statement.setString(6, role);

                // ExÃ©cuter la requÃªte
                int rowsInserted = statement.executeUpdate();
                if (rowsInserted > 0) {
                    JOptionPane.showMessageDialog(null, "Inscription rÃ©ussie !");
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Erreur lors de l'inscription : " + ex.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
            } 
        }catch (ClassNotFoundException ex) {
            System.out.println("Driver JDBC non trouvÃ© : " + ex.getMessage());
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed


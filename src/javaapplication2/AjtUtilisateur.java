    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nom = jTextField1.getText();
        String prenom = jTextField2.getText();
        String email = jTextField3.getText();
        String motDePasse = new String(jPasswordField1.getPassword());
        String hashedPassword = BCrypt.hashpw(motDePasse, BCrypt.gensalt());
        
        String role = null;
        
        if (jRadioButton1.isSelected()) {
            role = "Admin";
        } else if (jRadioButton2.isSelected()) {
            role = "Gestionnaire";
        }
        
        jLabel6.setText("");
        jLabel7.setText("");
        jLabel8.setText("");
        jLabel9.setText("");
        
        boolean hasError = false;

        // Validate each field and set corresponding error label if empty
        if (jTextField1.getText().isEmpty()) {
            jLabel6.setText("*Ce champ est obligatoire.");
            hasError = true;
        }
        if (jTextField2.getText().isEmpty()) {
            jLabel7.setText("*Ce champ est obligatoire.");
            hasError = true;
        }
        if (jTextField3.getText().isEmpty()) {
            jLabel8.setText("*Ce champ est obligatoire.");
            hasError = true;
        }
        if (jPasswordField1.getPassword().length == 0) {
            jLabel9.setText("*Ce champ est obligatoire.");
            hasError = true;
        }
        if (hasError) {
            return;
        }
        
        try (Connection conn = DatabaseConnection.connect()) {
                // Pr√©parer la requ√™te SQL
                String sql = "INSERT INTO utilisateur (nom, prenom, email, mot_de_passe, role) VALUES (?, ?, ?, ?, ?)";
                PreparedStatement statement = conn.prepareStatement(sql);
                statement.setString(1, nom);
                statement.setString(2, prenom);
                statement.setString(3, email);
                statement.setString(4, hashedPassword);
                statement.setString(5, role);

                // Ex√©cuter la requ√™te
                int rowsInserted = statement.executeUpdate();
                if (rowsInserted > 0) {
                    JOptionPane.showMessageDialog(null, "Inscription r√©ussie !");
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Erreur lors de l'inscription : " + ex.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
            } 
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

